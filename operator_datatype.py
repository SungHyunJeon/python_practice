"""연산자(operator), data type, type 변환"""

ddd = 1 + 4
print(ddd) # 5로 출력된다.


eee = 'hello ' + 'there'
print(eee) # hello there로 출력된다.

eee = eee + 1 # 문자열 타입과 정수형 타입을 더하려 해서 에러발생.



type(eee) # clas 'str' 문자열 클래스 타입
type(1) # 정수(integer) 클래스 타입


