#One-Way Decisions

x = 5
print('Before 5')
if x == 5 :            #Before 5
    print('Is 5')        #Is 5
    print('Is Still 5')#Is Still 5
    print('Third 5')    #Third 5
print('Afterwards 5')    #Afterwards 5
print('Before 6')        #Before 6
if x == 6 :
    print('Is 6')        #Is 6
    print('Is Still 6')    #Is Still 6
    print('Third 6')        #Third 6
print('Afterwards 6')        #Afterwards 6
