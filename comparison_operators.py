# comparison_operators

x = 5
print('x = 5')
if x == 5 :
    print('Equals 5')        #Equals5
if x > 4 :
    print('Greater than 4')    #Greater than 4
if x >= 5 :
    print('Greater than or Equals 5')  #Greater than or Equals5
if x < 6 :
    print('Less than 6')        #Less than 6
if x <= 5 :
    print('Less than or Equals 5') #Less than or Equals 5
if x != 6 :
    print('Not equal 6')    #Not equal 6
